package com.xyd.student.xydexamanalysis.entity;

public class Login {
	private Login_userInfo userInfoList;
	private int resultCode;
	private String resultDesc;

	public Login() {
		super();
	}

	public Login(Login_userInfo userInfoList, int resultCode, String resultDesc) {
		super();
		this.userInfoList = userInfoList;
		this.resultCode = resultCode;
		this.resultDesc = resultDesc;
	}

	/**
	 * @return the userInfoList
	 */
	public Login_userInfo getUserInfoList() {
		return userInfoList;
	}

	/**
	 * @param userInfoList
	 *            the userInfoList to set
	 */
	public void setUserInfoList(Login_userInfo userInfoList) {
		this.userInfoList = userInfoList;
	}

	/**
	 * @return the resultCode
	 */
	public int getResultCode() {
		return resultCode;
	}

	/**
	 * @param resultCode
	 *            the resultCode to set
	 */
	public void setResultCode(int resultCode) {
		this.resultCode = resultCode;
	}

	/**
	 * @return the resultDesc
	 */
	public String getResultDesc() {
		return resultDesc;
	}

	/**
	 * @param resultDesc
	 *            the resultDesc to set
	 */
	public void setResultDesc(String resultDesc) {
		this.resultDesc = resultDesc;
	}

}
