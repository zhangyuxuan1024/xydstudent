package com.xyd.student.xydexamanalysis.entity;

public class videoList {
	private String wkId;
	private String wkName;

	public videoList() {
		super();
	}

	public videoList(String wkId, String wkName) {
		super();
		this.wkId = wkId;
		this.wkName = wkName;
	}

	/**
	 * @return the wkId
	 */
	public String getWkId() {
		return wkId;
	}

	/**
	 * @param wkId
	 *            the wkId to set
	 */
	public void setWkId(String wkId) {
		this.wkId = wkId;
	}

	/**
	 * @return the wkName
	 */
	public String getWkName() {
		return wkName;
	}

	/**
	 * @param wkName
	 *            the wkName to set
	 */
	public void setWkName(String wkName) {
		this.wkName = wkName;
	}

}
